# 1. 初始赋值
t = (1, 2, 3)
print("初始t：", t)  # (1, 2, 3)

# 2. 尝试直接改元素（报错，验证元组不可变）
try:
    t[1] = 20
except TypeError as e:
    print("直接改元素报错：", e)  # 输出报错信息

# 3. 修改元组变量（重新赋值）
t = (1, 20, 3)  # 指向新元组
print("修改变量后t：", t)  # (1, 20, 3)

# 4. 拼接生成新元组
t = t + (4, 5)  # 用旧t的新元组，再拼接生成更全新的元组
print("拼接后t：", t)  # (1, 20, 3, 4, 5)